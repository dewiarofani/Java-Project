    private void jbuttonTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbuttonTotalActionPerformed
        // TODO add your handling code here:
        // nilai pada textfield dimasukan pada tiap2 array
        itemcost[0] = Integer.parseInt(jtxtFriedChicken.getText());
        itemcost[1] = Integer.parseInt(jtxtBurger.getText());
        itemcost[2] = Integer.parseInt(jtxtSandwich.getText());
        itemcost[3] = Integer.parseInt(jtxtFrenchFries.getText());
        itemcost[4] = Integer.parseInt(jtxtOnionRing.getText());
        itemcost[5] = Integer.parseInt(jtxtPepsi.getText());
        itemcost[6] = Integer.parseInt(jtxtIcedTea.getText());
        itemcost[7] = Integer.parseInt(jtxtCoffee.getText());
        itemcost[8] = Integer.parseInt(jtxtOrangeJuice.getText());
        itemcost[9] = Integer.parseInt(jtxtMineralWater.getText());
        
        // menghitung sub total
        iSubTotal = Price_FriedChicken*itemcost [0] + Price_Burger*itemcost [1] 
                + Price_Sandwich*itemcost [2] + Price_FrenchFries*itemcost [3] 
                + Price_OnionRing*itemcost [4] + Price_Pepsi*itemcost [5] + Price_IcedTea*itemcost [6] 
                + Price_Coffee*itemcost [7] + Price_OrangeJuice*itemcost [8] 
                + Price_MineralWater*itemcost [9] ;
        
        // menghitung pajak
        iTax = (iSubTotal * 10)/100;
        
        // menghitung total keseluruhan
        iTotal = iSubTotal + iTax;
            
            // mengoutputkan nilai tiap-tiap perhitungan pada masing2 textfield
            String cTax = String.format("Rp%d", iTax);
            jtxtTax.setText(cTax);
        
            String cSubTotal = String.format("Rp%d", iSubTotal);
            jtxtSubTotal.setText(cSubTotal);
        
            String cTotal = String.format("Rp%d", iTotal);
            jtxtTotal.setText(cTotal);
    }//GEN-LAST:event_jbuttonTotalActionPerformed

