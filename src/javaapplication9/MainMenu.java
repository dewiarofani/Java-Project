    private void jbuttonChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbuttonChangeActionPerformed
        // TODO add your handling code here:
        // variabel iPay berisi nilai pada textfield pay
        iPay = Integer.parseInt(jtxtPay.getText()); 
        // variabel string berisi item yang kita pilih
        String PaymentMethod = (String)jcmbpaymethod.getSelectedItem();
        
        if (PaymentMethod == "Cash"){// jika memilih cash maka
            if (iPay >= iTotal){ // dan jika nilai iPay >= iTotal maka
                // mengoutputkan iPay - iTotal pada textfield change
                String cChange = String.format("Rp%d", iPay - iTotal);
                jtxtChange.setText(cChange);
            } // jika nilai iPay kurang maka
            else {
                // menampilkan message dialog "not enough cash"
                JOptionPane.showMessageDialog(null, "Not enough cash entered", 
                        "zimS Fast Food", JOptionPane.OK_OPTION);
            }
        }
        
        //jika memilih lainnya (debit card and qris) maka
        else {
            // hanya mengoutputkan iPay - iTotal pada textfield change
            String cChange = String.format("Rp%d", iPay - iTotal);
            jtxtChange.setText(cChange);
        }
    }//GEN-LAST:event_jbuttonChangeActionPerformed
    
    private void jbuttonOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbuttonOrderActionPerformed
        // TODO add your handling code here:
        String PaymentMethod = (String)jcmbpaymethod.getSelectedItem();
        
        //jika kita klik tombol order dan memilih cash, maka pesan yg ditampilkan
        if (PaymentMethod == "Cash"){
            JOptionPane.showMessageDialog(null, "Terima kasih Atas Pesanan Anda"+ "\n"
                    +"Pesanan Anda Akan Kami Proses");
        }
        
        //jika kita klik tombol order dan memilih debit card, maka pesan yg ditampilkan
        else if (PaymentMethod == "Debit Card"){
            JOptionPane.showMessageDialog(null, "Enter your debit card, please");
            JOptionPane.showMessageDialog(null, "Terima kasih Atas Pesanan Anda"+"\n"
                    +"Pesanan Anda Akan Kami Proses");
        }
        
        //jika kita klik tombol order dan memilih qris, maka pesan yg ditampilkan
        else {
            JOptionPane.showMessageDialog(null, "Scan the barcode, please");
            JOptionPane.showMessageDialog(null, "Terima kasih Atas Pesanan Anda"+"\n"
                    +"Pesanan Anda Akan Kami Proses");
        }
        
    }//GEN-LAST:event_jbuttonOrderActionPerformed
    
    private void jbuttonCetakPesananActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbuttonCetakPesananActionPerformed
        // TODO add your handling code here:
        String foods, drinks;
        
        //Foods
        StringBuffer var = new StringBuffer ();
        if (jchkFriedChicken.isSelected()) { // jika checkbox dichecklist, maka
            // menambahkan text pada checkbox dan nilai pada textfield ke dalam var
            var.append("\n");
            var.append(jchkFriedChicken.getText());
            var.append("\t");
            var.append(jtxtFriedChicken.getText());
        }  
        if (jchkBurger.isSelected()) {
            var.append("\n");
            var.append(jchkBurger.getText());
            var.append("\t");
            var.append(jtxtBurger.getText());
        }  
        if (jchkSandwich.isSelected()) {
            var.append("\n");
            var.append(jchkSandwich.getText());
            var.append("\t");
            var.append(jtxtSandwich.getText());
        }  
        if (jchkFrenchFries.isSelected()) {
            var.append("\n");
            var.append(jchkFrenchFries.getText());
            var.append("\t");
            var.append(jtxtFrenchFries.getText());
        }  
        if (jchkOnionRing.isSelected()) {
            var.append("\n");
            var.append(jchkOnionRing.getText());
            var.append("\t");
            var.append(jtxtOnionRing.getText());
        }
        // lalu program di atas disimpan pada variabel baru utk memudahkan pemanggilan
        foods = var.toString();
        
        //Drinks
        StringBuffer dri = new StringBuffer ();
        if (jchkPepsi.isSelected()) {
            dri.append("\n");
            dri.append(jchkPepsi.getText());
            dri.append("\t");
            dri.append(jtxtPepsi.getText());
        }  
        if (jchkIcedTea.isSelected()) {
            dri.append("\n");
            dri.append(jchkIcedTea.getText());
            dri.append("\t");
            dri.append(jtxtIcedTea.getText());
        }  
        if (jchkCoffee.isSelected()) {
            dri.append("\n");
            dri.append(jchkCoffee.getText());
            dri.append("\t");
            dri.append(jtxtCoffee.getText());
        }  
        if (jchkOrangeJuice.isSelected()) {
            dri.append("\n");
            dri.append(jchkOrangeJuice.getText());
            dri.append("\t");
            dri.append(jtxtOrangeJuice.getText());
        }  
        if (jchkMineralWater.isSelected()) {
            dri.append("\n");
            dri.append(jchkMineralWater.getText());
            dri.append("\t");
            dri.append(jtxtMineralWater.getText());
        }  
        drinks = dri.toString();
        
        // mengoutputkan pada textarea
        jtxtArea.setText(
        "Foods\t: " +foods+ "\n"+
        "\nDrinks\t: " +drinks+ "\n"+
        "\nSub Total\t: "+jtxtSubTotal.getText()+"\n"+
        "Tax\t: "+jtxtTax.getText()+"\n"+
        "Total\t: "+jtxtTotal.getText()+"\n"+
        "Pay\t: Rp"+jtxtPay.getText()+"\n"+
        "Change\t: "+jtxtChange.getText()+"\n");
    }//GEN-LAST:event_jbuttonCetakPesananActionPerformed

